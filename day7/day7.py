def count_splits(diagram: str) -> int:
    grid = [list(line) for line in diagram.strip().splitlines()]
    height = len(grid)
    width = len(grid[0])
    # Find the starting position 'S'
    for y, row in enumerate(grid):
        for x, cell in enumerate(row):
            if cell == 'S':
                start_x, start_y = x, y
                break

    from collections import deque

    # Each beam is (x, y, dx), where dx is direction: 0=down, -1=left, 1=right
    queue = deque()
    queue.append((start_x, start_y + 1, 0))
    visited = set()
    splits = 0

    while queue:
        x, y, dx = queue.popleft()
        if not (0 <= x < width and 0 <= y < height):
            continue
        state = (x, y, dx)
        if state in visited:
            continue
        visited.add(state)
        cell = grid[y][x]
        if cell == '^':
            splits += 1
            if x > 0:
                queue.append((x - 1, y + 1, -1))
            if x < width - 1:
                queue.append((x + 1, y + 1, 1))
        elif cell == '.':
            queue.append((x, y + 1, 0))
        # Ignore other cells
    return splits

def count_timelines(diagram: str) -> int:
    grid = [list(line) for line in diagram.strip().splitlines()]
    height = len(grid)
    width = len(grid[0])
    # Find the starting position 'S'
    for y, row in enumerate(grid):
        for x, cell in enumerate(row):
            if cell == 'S':
                start_x, start_y = x, y
                break

    from functools import lru_cache

    @lru_cache(maxsize=None)
    def dfs(x, y):
        if not (0 <= x < width and 0 <= y < height):
            # Out of bounds: one timeline ends here
            return 1
        cell = grid[y][x]
        if cell == '^':
            left = dfs(x - 1, y + 1) if x > 0 else 0
            right = dfs(x + 1, y + 1) if x < width - 1 else 0
            return left + right
        elif cell == '.':
            return dfs(x, y + 1)
        else:
            # Unknown cell, treat as one timeline ending here
            return 1

    return dfs(start_x, start_y + 1)

